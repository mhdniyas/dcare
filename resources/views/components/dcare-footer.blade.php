<script>
function footerComponent() {
    return {
        email: '',
        newsletterMessage: '',
        newsletterSuccess: false,
        
        async subscribeNewsletter() {
            if (!this.email.trim()) {
                this.newsletterMessage = 'Please enter your email address';
                this.newsletterSuccess = false;
                return;
            }
            
            if (!this.isValidEmail(this.email)) {
                this.newsletterMessage = 'Please enter a valid email address';
                this.newsletterSuccess = false;
                return;
            }
            
            try {
                // Simulate API call
                const response = await fetch('/api/newsletter/subscribe', {
                    method: 'POST',
                    headers: {
                        'Content-Type': 'application/json',
                        'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').getAttribute('content')
                    },
                    body: JSON.stringify({ email: this.email })
                });
                
                if (response.ok) {
                    this.newsletterMessage = 'Thank you for subscribing!';
                    this.newsletterSuccess = true;
                    this.email = '';
                } else {
                    throw new Error('Subscription failed');
                }
            } catch (error) {
                this.newsletterMessage = 'Subscription failed. Please try again.';
                this.newsletterSuccess = false;
            }
        },
        
        isValidEmail(email) {
            const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            return emailRegex.test(email);
        }
    }
}
</script>
